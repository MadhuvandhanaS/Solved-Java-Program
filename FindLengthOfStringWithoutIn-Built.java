import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        String s = in.nextLine();
        int l = 0;
        for(int i=0;i<s.length();i++){
            l++;
        }
        System.out.println(l);
    }
}
